    @Override
    public void initialize() {
        jLabel4.setText("Aguardando seleção");
        jLabel6.setText("Aguardando seleção");
        jComboBox1.removeAllItems();
        jComboBox1.removeAll();
        listafuncoes = FuncaoController.getInstance().retornaListaFuncoes();
        if (listafuncoes == null) {
            int cadastro = javax.swing.JOptionPane.showConfirmDialog(null, "Nenhuma função cadastrada, deseja fazer isso agora?", "Funções", JOptionPane.YES_NO_OPTION);
            if (cadastro == 0) {
                TelaCadastraFuncao.getInstance().setInterceptor(this);
                TelaCadastraFuncao.getInstance().setVisible(true);
                this.setVisible(false);
            } else {
                this.formWindowClosing(null);
            }
        } else {
            for (funcao object : listafuncoes) {
                jComboBox1.addItem(object);
            }
        }
    }

