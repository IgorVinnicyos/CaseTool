    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (func != null) {
            if (PessoaController.getInstance().returnPesLogin().getNivel_acesso() == 1 || PessoaController.getInstance().returnPesLogin().getNivel_acesso() == 2) {
                func = (funcao) jComboBox1.getSelectedItem();
                FuncaoController.getInstance().deletar(func.getIdfuncao());
                this.initialize();
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Erro, você não possui nivel de acesso suficiente para esta operação!");
            }

        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Selecione uma função!");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

