    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (func != null) {
            if(!EquipeController.getInstance().verificaFuncaoEmUso(func.getIdfuncao())){
                javax.swing.JOptionPane.showMessageDialog(null, "Erro, está função está em uso em projetos!");
            }else{
                TelaEditarFuncao.getInstance().setInterceptor(this);
                func = (funcao) jComboBox1.getSelectedItem();
                TelaEditarFuncao.getInstance().setFunc(func);
                TelaEditarFuncao.getInstance().initialize();
                TelaEditarFuncao.getInstance().setVisible(true);
                this.setVisible(false);
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Selecione uma função!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

